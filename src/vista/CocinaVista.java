    private String ingredientesToString(java.util.List<modelo.Ingrediente> ingredientes) {
        if (ingredientes == null || ingredientes.isEmpty()) return "-";
        StringBuilder sb = new StringBuilder();
        for (modelo.Ingrediente ing : ingredientes) {
            if (!esBebida(ing)) {
                sb.append(ing.getNombre()).append(", ");
            }
        }
        if (sb.length() > 2) sb.setLength(sb.length() - 2); // Quita la última coma
        return sb.toString();
    }

    private String extraerBebidas(java.util.List<modelo.Ingrediente> ingredientes) {
        if (ingredientes == null) return "-";
        StringBuilder sb = new StringBuilder();
        for (modelo.Ingrediente ing : ingredientes) {
            if (esBebida(ing)) {
                sb.append(ing.getNombre()).append(", ");
            }
        }
        if (sb.length() > 2) sb.setLength(sb.length() - 2);
        return sb.length() > 0 ? sb.toString() : "-";
    }

    private boolean esBebida(modelo.Ingrediente ing) {
        String nombre = ing.getNombre().toLowerCase();
        return nombre.contains("coca") || nombre.contains("pepsi") || nombre.contains("inca") || nombre.contains("sprite");
    }


// Muestra los datos del pedido en los labels del panel jp_pedido3
public void mostrarNuevoPedido(modelo.Pedido pedido) {
    // Mostrar datos básicos del pedido
    jl_numero_pedido3.setText(pedido.getId());
    jl_hora_pedido3.setText(pedido.getFechaHora().toLocalTime().toString());
    jl_estado_pedido3.setText(pedido.getEstado());
    jl_tipo_pedido3.setText(pedido.getNombrePizza());

    // Mostrar cantidad: si viene de InicioVista, úsala; si no, usa la del objeto Pedido
    if (inicioVista != null) {
        jl_cantidad_pedido3.setText(inicioVista.getCantidadSeleccionada());
    } else {
        jl_cantidad_pedido3.setText(String.valueOf(pedido.getCantidad()));
    }

    // Mostrar tamaño y masa
    jl_tamanio_pedido3.setText(pedido.getTamanio());
    jl_masa_pedido3.setText(pedido.getMasa());

    // Mostrar ingredientes
    jl_ingredientes_pedido3.setText(ingredientesToString(pedido.getIngredientes()));

    // Mostrar bebida (si la tienes implementada como ingrediente tipo bebida)
    jl_bebida_pedido3.setText(extraerBebidas(pedido.getIngredientes()));

    // Mostrar tipo de consumo (local / para llevar)
    jl_entrega_pedido3.setText(pedido.getTipoConsumo());

    // Mostrar el panel del pedido
    jp_pedido3.setVisible(true);
}
